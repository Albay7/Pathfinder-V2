            return optionsHTML;
        }

        function validateCurrentPage() {
            console.log('Validating page:', currentPage);

            const currentPageElement = document.getElementById(`page-${currentPage}`);
            if (!currentPageElement) {
                console.log('Page element not found:', `page-${currentPage}`);
                return false;
            }

            // Get all unique question names on this page
            const questionNames = new Set();
            const allInputs = currentPageElement.querySelectorAll('input[type="radio"]');
            console.log('Found radio inputs:', allInputs.length);
            allInputs.forEach(input => questionNames.add(input.name));
            console.log('Question names on page:', Array.from(questionNames));

            // Check if each question has been answered
            for (let questionName of questionNames) {
                const answered = currentPageElement.querySelector(`input[name="${questionName}"]:checked`);
                console.log(`Question ${questionName} answered:`, !!answered);
                if (!answered) {
                    // Use SweetAlert for better user experience
                    Swal.fire({
                        icon: 'warning',
                        title: 'Question Required',
                        text: `Please answer question ${questionName.replace('q', '')} before proceeding.`,
                        confirmButtonColor: '#5AA7C6'
                    });
                    return false;
                }
            }
            console.log('All questions answered on page', currentPage);
            return true;
        }

        function saveCurrentPageResponses() {
            const currentPageElement = document.getElementById(`page-${currentPage}`);
            const inputs = currentPageElement.querySelectorAll('input[type="radio"]:checked');

            inputs.forEach(input => {
                responses[input.name] = parseInt(input.value);
            });
        }

        function updateProgress() {
