        function showPage(pageNum) {
            console.log('Showing page:', pageNum);

            // Hide all pages
            document.querySelectorAll('.question-page').forEach(page => {
                page.classList.remove('active');
            });

            // Show current page or create it if it doesn't exist
            let currentPageElement = document.getElementById(`page-${pageNum}`);
            if (!currentPageElement) {
                console.log('Creating page:', pageNum);
                currentPageElement = createPage(pageNum);
            }
            currentPageElement.classList.add('active');
            console.log('Page shown successfully');

            // Update step indicators
            document.querySelectorAll('.step-indicator').forEach((indicator, index) => {
                indicator.classList.remove('active', 'completed');
                if (index + 1 === pageNum) {
                    indicator.classList.add('active');
                } else if (index + 1 < pageNum) {
                    indicator.classList.add('completed');
                }
            });
        }

        function createPage(pageNum) {
            const startQuestion = (pageNum - 1) * questionsPerPage + 1;
            const endQuestion = Math.min(startQuestion + questionsPerPage - 1, 60);

            const pageDiv = document.createElement('div');
            pageDiv.className = 'question-page';
            pageDiv.id = `page-${pageNum}`;

            const questionsDiv = document.createElement('div');
            questionsDiv.className = 'space-y-8';

            for (let i = startQuestion; i <= endQuestion; i++) {
                const questionDiv = createQuestionElement(i, questions[i]);
                questionsDiv.appendChild(questionDiv);
            }

            pageDiv.appendChild(questionsDiv);

            // Insert into the questions container
            const questionsContainer = document.getElementById('questions-container');
            if (questionsContainer) {
                questionsContainer.appendChild(pageDiv);
            } else {
                // Fallback: Insert before navigation buttons
                const form = document.getElementById('mbti-questionnaire-form');
                const navButtons = form.querySelector('div.flex.justify-between');
                form.insertBefore(pageDiv, navButtons);
            }

            return pageDiv;
        }

