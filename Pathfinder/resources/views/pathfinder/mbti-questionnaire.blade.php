            values.forEach((value, index) => {
                const colorClass = value >= 5 ? 'teal' : value === 4 ? 'gray' : 'blue';
                const shadowClass = value === 7 || value === 1 ? 'hover:shadow-lg' : 'hover:shadow-md';

                const customStyle = value <= 3 ? `style="background: linear-gradient(to bottom right, #EFF6FF, #DBEAFE); border-color: #5AA7C6;"` :
