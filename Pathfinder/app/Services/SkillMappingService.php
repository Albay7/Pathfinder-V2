    /**
     * Get skills for a specific role
     */
    public static function getSkillsForRole($role)
    {
        $mapping = self::getRoleSkillMapping();

        // Return old structure for backward compatibility
        // If role has new categorized structure, merge all skills
        if (isset($mapping[$role])) {
            $roleData = $mapping[$role];

            // Check if it's using the new categorized structure
            if (isset($roleData['fundamental_skills'])) {
                return [
                    'fundamental_skills' => $roleData['fundamental_skills'] ?? [],
                    'medium_skills' => $roleData['medium_skills'] ?? [],
                    'advanced_skills' => $roleData['advanced_skills'] ?? [],
                    'soft_skills' => $roleData['soft_skills'] ?? [],
                    'skill_weights' => $roleData['skill_weights'] ?? []
                ];
            }

            // Old structure (for non-IT roles)
            return $roleData;
        }

        return ['technical_skills' => [], 'soft_skills' => []];
    }

    /**
     * Get skill importance weight for a specific skill in a role
     *
     * @param string $role The role name
     * @param string $skill The skill name
     * @return int Weight from 1-5 (5=Critical, 4=Very Important, 3=Important, 2=Somewhat, 1=Low)
     */
    public static function getSkillImportanceWeight($role, $skill): int
    {
        $mapping = self::getRoleSkillMapping();

        if (isset($mapping[$role]['skill_weights'][$skill])) {
            return $mapping[$role]['skill_weights'][$skill];
        }

        return 3; // Default: moderate importance
    }

    /**
     * Get all skills for a role (combined list)
     *
     * @param string $role The role name
     * @return array Combined array of all skills (fundamental + medium + advanced + soft)
     */
    public static function getAllSkillsForRole($role): array
    {
        $mapping = self::getRoleSkillMapping();

        if (!isset($mapping[$role])) {
            return [];
        }

        $roleData = $mapping[$role];

        // Check if it's using the new categorized structure
        if (isset($roleData['fundamental_skills'])) {
            return array_merge(
                $roleData['fundamental_skills'] ?? [],
                $roleData['medium_skills'] ?? [],
                $roleData['advanced_skills'] ?? [],
                $roleData['soft_skills'] ?? []
            );
        }

        // Old structure (for non-IT roles)
        return array_merge(
            $roleData['technical_skills'] ?? [],
            $roleData['soft_skills'] ?? []
        );
    }

